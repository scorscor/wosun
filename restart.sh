#!/bin/bash

# 莴笋录屏网站 Docker 快速重启脚本（不重新构建）

echo "=========================================="
echo "快速重启莴笋录屏网站"
echo "=========================================="

# 停止容器
echo "1. 停止容器..."
docker compose stop

# 启动容器
echo "2. 启动容器..."
docker compose start

# 检查容器状态
echo "3. 检查容器状态..."
docker compose ps

echo "=========================================="
echo "重启完成！"
echo "=========================================="
