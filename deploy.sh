#!/bin/bash

# 莴笋录屏网站 Docker 部署脚本

echo "=========================================="
echo "开始部署莴笋录屏网站"
echo "=========================================="

# 停止并删除旧容器
echo "停止并删除旧容器..."
docker compose down

# 重新构建并启动容器
echo "重新构建并启动容器..."
docker compose up -d --build

echo "=========================================="
echo "部署完成！"
echo "=========================================="
